/****************************************************************************************
 * Subroutine to send a string of text to the LCD
****************************************************************************************/
void LCD_text (char* text_ptr);

/****************************************************************************************
 * Subroutine to turn off the LCD cursor
****************************************************************************************/
void LCD_cursor_off (void);

/****************************************************************************************
 * Subroutine to clear the LCD cursor
****************************************************************************************/
void LCD_clear (void);

#endif
